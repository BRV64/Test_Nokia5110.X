void LcdCharacter(char character)
{
    lcd_data_write(0x00);
    int index;
    for ( index = 0; index < 5; index++)
    {
        lcd_data_write(ASCII[character - 0x20][index]);
    }
    lcd_data_write(0x00);
}
 
 
void LcdString(char *characters)
{
    while (*characters)
    {
        LcdCharacter(*characters++);
    }
}
/**
* gotoXY routine to position cursor
* x - range: 0 to 84
* y - range: 0 to 5
*/
 
void gotoXY(int x, int y)   //goto function
{
    lcd_command_write(0x80 | x); // Column.
    lcd_command_write(0x40 | y); // Row.
}
 
 
void drawBox(void)
{
    int j;
    for(j = 0; j < 84; j++) // top
    {
        gotoXY(j, 0);
        lcd_data_write(0x01);
    }
    for(j = 0; j < 84; j++) //Bottom
    {
        gotoXY(j, 5);
        lcd_data_write(0x80);
    }
    for(j = 0; j < 6; j++) // Right
    {
        gotoXY(83, j);
        lcd_data_write(0xff);
    }
    for(j = 0; j < 6; j++) // Left
    {
        gotoXY(0, j);
        lcd_data_write(0xff);
    }
}
void LcdClear(void)
{
    int index;
    for (index = 0; index < LCD_X * LCD_Y / 8; index++)
    {
        lcd_data_write(0x00);
    }
}
void setup()
{
    LcdClear();
    gotoXY(7,1);
    LcdString("Hello");
}
 
int main()
{

    board_init();
    spi_init();

    lcd_init();
    LcdClear();

    while(1);
    {
        setup();
    }
    return 1;
}