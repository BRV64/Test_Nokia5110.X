void board_init(void)
{
//disable analog functionality

    AD1PCFG=0xffff; //disable analog functionality.
    // disable parallel mode
    _PMPEN=0; // disable parallel module
    // gpio pins directions are set.
    lcd_reset_tris=0;
    lcd_a0_tris=0;
    lcd_cs_tris=0;

    lcd_sdo_tris=0;
    lcd_sck_tris=0;
    // mapped the spi module to the pins.
    PPSUnLock;

    _RP13R=7;
    _RP15R=8;
    PPSLock;

    // PORTAbits.RA4 =0;
    // PORTAbits.RA0 =0;
    // PORTBbits.RB4 =0;
    //interrupt characters
    _NSTDIS=1;
    // disable nested interrupt.
    TRISBbits.TRISB13=0;
    TRISBbits.TRISB15=0;
}
 
 
void spi_init(void)    //spi initialisation
{

    SPI1CON1= 0x003e;
/*
* CKE polarity from idle clock state to acive.
* 
*/
/*
* 
* CKP =0 Idle state for clock is low level and active state is high level.
*
*/
    SPI1CON2= 0;
//SPI1CON1bits.CKP=1;
//enable the spi module.
    SPI1STAT= 0x8000;
//1000 0000 0000 0000
}

char spi_write(char data)
{
    lcd_cs=0;
    do{
        if(SPI1STATbits.SPIROV)
        SPI1STATbits.SPIROV=0;
    }while(SPI1STATbits.SPIROV);
    while(SPI1STATbits.SPITBF);
    SPI1BUF=data;
    while(!SPI1STATbits.SPIRBF);
    return SPI1BUF;
    lcd_cs=1;
// PORTBbits.RB4 =1;
}
 
 
#define lcd_command_write(x) {lcd_cs=0;lcd_a0=0; spi_write(x);lcd_cs=1;}
#define lcd_data_write(x) {lcd_cs=0;lcd_a0=1;spi_write(x);lcd_cs=1;}

void lcd_init()        //initialisation of LCD
{
    lcd_reset_tris=0;
    lcd_a0_tris=0;
    lcd_cs_tris=0;
    lcd_sdo_tris=0;
    lcd_sck_tris=0;
    lcd_a0=0;
    lcd_cs=0;
    // PORTAbits.RA4=0;
    //PORTAbits.RA0 =0;
    // PORTBbits.RB4 =0;
    lcd_reset=0;
    delay_ms(100);
    lcd_reset=1;
    lcd_command_write(0x21);//Tell LCD that extended commands follow
    delay_ms(10);
    lcd_command_write(0xBF);//Set LCD Vop (Contrast): Try 0xB1(good @ 3.3V) or 0xBF if your display is too dark
    delay_ms(10);
    lcd_command_write(0x04);//Set Temp coefficent
    delay_ms(10);
    lcd_command_write(0x14);//LCD bias mode 1:48: Try 0x13 or 0x14
    delay_ms(10);
    lcd_command_write(0x0C);//We must send 0x20 before modifying the display control mode
    delay_ms(10);
    lcd_command_write(0x20);//Set display control, normal mode. 0x0D for inverse
    delay_ms(10);
    lcd_command_write(0x0C);
    delay_ms(10);
    lcd_cs=1;
}
 
